export function getBreadcrumbs(path: string): RouteConfig[] {
  const crumbs: RouteConfig[] = [];
  let current = Object.values(ROUTES).find((r) => r.path === path);
  while (current) {
    crumbs.unshift(current);
    current = current.parent
      ? Object.values(ROUTES).find((r) => r.path === current!.parent)
      : undefined;
  }
  return crumbs;
}

